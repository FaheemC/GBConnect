            <h3>Computer Systems Technician Program (T141) Your Career </h3>

            <p>This program prepares graduates for jobs in a wide variety of positions, including systems administrators connected with wireless LAN support, wireless service providers and cellular and broadband providers.
                Opportunities also exist in companies that provide computer support services to other businesses, and in organizations that specialize in the sale and installation of computer systems and networks.
                Graduates may also find employment working directly with business clients, providing training services and ongoing support to assist them in using computer applications effectively.
                Graduates will be prepared to write several of the examinations leading to industry certification in Network Administration and Support.</p>

            <h3>Future Study Options </h3>

            <p>Students who successfully complete this program may qualify for entry into Ontario college graduate certificate and university degree programs.
                George Brown currently has articulation agreements with various other post-secondary institutions such as the University of Ontario Institute of Technology,
                McMaster University and the Northern Alberta Institute of Technology.</p>

