            <h3> Computer Programmer Analyst Program (T127) Your Career </h3>

            <p> Graduates can look forward to career opportunities such as database administration, systems analysis and design, consulting, application design and development,
                Internet application design and development, and many others.
                Opportunities exist for systems development for both corporate clients and smaller contract companies.
                As programmer analysts, graduates will work in project teams responsible for the development of new applications and the maintenance of existing business systems.</p>

