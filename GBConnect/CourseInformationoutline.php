            <h3>Computer Systems Technician Program (T141) Your Career </h3>

            <p>Graduates can provide client support and network technical support in a wide variety of job situations and industries.
                Opportunities exist in companies that provide computer support services to other businesses, and in organizations that specialize in the sale and installation of computer systems and networks.
                Graduates may also find employment working directly with business clients, providing training services and ongoing support to assist them in using computer applications effectively.
                The curriculum also prepares students to, after graduating from the program, write several of the examinations leading to industry certification in Network Administration and Support.</p>

            <h3>Future Study Options </h3>

            <p>Graduates may continue directly into the third year of the , and earn an Ontario College Advanced Diploma in two additional semesters.
                Students who successfully complete this program may qualify for entry into university degree programs.
                This includes credit towards the Athabasca University BSc-CIS (PD).</p>

